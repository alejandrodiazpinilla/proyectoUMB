<?php


namespace App\Http\Controllers;

class HelpersClass{

    // ELIMINAS COMILLA SENCILLA DE STRING PARA CORREGIR ERROR CARGA DE VARIABLE POR SQL
    public static function sql_sanitaze($cadena){
        
        $cadena = str_replace(
        array("'", "&#39;",),
        array("-", " "),
        $cadena
        );
    
        return $cadena;
    }

    // ELIMINAS ACENTOS Y ELIMINAR ESPACIOS
    public static function utf8_remplace($cadena){
    
        //Reemplazamos la A y a
        $cadena = str_replace(
        array('Á', 'À', 'Â', 'Ä', 'á', 'à', 'ä', 'â', 'ª'),
        array('A', 'A', 'A', 'A', 'a', 'a', 'a', 'a', 'a'),
        $cadena
        );

        //Reemplazamos la E y e
        $cadena = str_replace(
        array('É', 'È', 'Ê', 'Ë', 'é', 'è', 'ë', 'ê'),
        array('E', 'E', 'E', 'E', 'e', 'e', 'e', 'e'),
        $cadena );

        //Reemplazamos la I y i
        $cadena = str_replace(
        array('Í', 'Ì', 'Ï', 'Î', 'í', 'ì', 'ï', 'î'),
        array('I', 'I', 'I', 'I', 'i', 'i', 'i', 'i'),
        $cadena );

        //Reemplazamos la O y o
        $cadena = str_replace(
        array('Ó', 'Ò', 'Ö', 'Ô', 'ó', 'ò', 'ö', 'ô'),
        array('O', 'O', 'O', 'O', 'o', 'o', 'o', 'o'),
        $cadena );

        //Reemplazamos la U y u
        $cadena = str_replace(
        array('Ú', 'Ù', 'Û', 'Ü', 'ú', 'ù', 'ü', 'û'),
        array('U', 'U', 'U', 'U', 'u', 'u', 'u', 'u'),
        $cadena );

        //Reemplazamos la N, n, C y c
        $cadena = str_replace(
        array('Ñ', 'ñ', 'Ç', 'ç'),
        array('N', 'n', 'C', 'c'),
        $cadena
        );
        
        //Reemplazamos Espacios, escencial para evitar que el usaurio se cree mal
        $cadena = str_replace(
        array(' '),
        array('_'),
        $cadena
        );

        return $cadena;
    }

}

